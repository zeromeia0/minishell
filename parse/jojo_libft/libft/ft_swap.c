void	ft_ult_swap(char **p1, char **p2)
{
	void	*temp;

	temp = *p1;
	*p1 = *p2;
	*p2 = temp;
}

void	ft_swap(char *p1, char *p2)
{
	int	temp;

	temp = *p1;
	*p1 = *p2;
	*p2 = temp;
}
