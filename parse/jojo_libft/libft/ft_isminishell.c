#include "../libft.h"

int	ft_isminishell(int c)
{
	if (ft_isalpha(c) || c == 33 || c == 35 || c == 37
		|| (c >= 42 && c <= 59) || c == 63 || c == 64)
		return (8);
	return (0);
}
