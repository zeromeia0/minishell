#include "../libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	if (lst == NULL || *lst == NULL)
		return ;
	if (del != NULL)
		ft_lstdelone(*lst, del);
	ft_lstclear (&((*lst)->next), NULL);
	free (*lst);
	*lst = NULL;
}
